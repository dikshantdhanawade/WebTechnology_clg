// toFixed()
// JS looks at decimal places
// Rounds the value
// Converts it into a STRING
let num=12.6789
console.log(num.toFixed(2))

// parseInt() / parseFloat()
// Extract numbers from user input / APIs
let num2=parseInt("100px");   // 100
parseFloat("99.99"); // 99.99
console.log(num2)

//Math.round()
//Used when normal rounding is required.
Math.round(4.6); // 5


//Math.floor()
// Used in:
// Pagination
// Discount calculations
Math.floor(4.9); // 4


//Math.max() / Math.min()
Math.max(10, 20, 5); // 20
Math.min(10, 20, 5); // 5


// Math.random()
Math.random(); // 0 to 0.9999


